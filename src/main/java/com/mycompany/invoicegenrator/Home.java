    private void dtlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dtlActionPerformed

    // Get the index of the currently selected row in the JTable
    int selectedRow = product.getSelectedRow();
    
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a product row to delete.", "No Selection", JOptionPane.WARNING_MESSAGE); //error warning 
        return;
    }

    try {
        // 1. Get the Product ID (Sr No) from the selected row.
        // ID is in the first column (index 0)
        Object idObject = product.getModel().getValueAt(selectedRow, 0);
        int productId = -1;
        
        if (idObject instanceof Integer) {
            productId = (Integer) idObject;
        } else {
             productId = Integer.parseInt(idObject.toString());
        }

        // 2. Confirm the deletion with the user
        int confirm = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete the selected product (ID: " + productId + ")?",
                "Confirm Deletion", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            // 3. Delete the record from the database
            String sql = "DELETE FROM products WHERE id = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setInt(1, productId);
            int rowsAffected = pst.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "üóëÔ∏è Product deleted successfully!");
                loadProducts(); // Refresh the JTable
            } else {
                JOptionPane.showMessageDialog(this, "Error: Product not found in database.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Error parsing Product ID from table: " + e.getMessage(), "Data Error", JOptionPane.ERROR_MESSAGE);
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Database Error during deletion: " + e.getMessage(), "SQL Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_dtlActionPerformed

