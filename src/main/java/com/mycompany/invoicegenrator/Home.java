    private void addpdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addpdActionPerformed
   String productName = produc.getText();
    String priceText = price.getText();
    String qtyText = qyt.getText();
    String gstValue = gst.getSelectedItem().toString();
    
    if (productName.isEmpty() || priceText.isEmpty() || qtyText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill all product details!");
        return;
    }
    try {
        double priceValue = Double.parseDouble(priceText);
        int qtyValue = Integer.parseInt(qtyText);

        // üßÆ Extract GST numeric value (remove % and convert to number)
        double gstPercent = Double.parseDouble(gstValue.replace("%", ""));
        
        // üßÆ Calculate total = (price √ó quantity) + GST%
        double subtotal = priceValue * qtyValue;
        double gstAmount = subtotal * (gstPercent / 100);
        double total = subtotal + gstAmount;

        // üóÉÔ∏è Insert into database
        String sql = "INSERT INTO products (PRODUCT_NAME, PRICE, QYNT, GST, TOTAL, DATE) VALUES (?, ?, ?, ?, ?, NOW())";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, productName);
        pst.setDouble(2, priceValue);
        pst.setInt(3, qtyValue);
        pst.setDouble(4, gstPercent);
        pst.setDouble(5, total);
        pst.executeUpdate();

        JOptionPane.showMessageDialog(this, "‚úÖ Product added successfully!"); // msg notification
        loadProducts();

        produc.setText("");
        price.setText("");
        qyt.setText("");

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Invalid number format for price or quantity!");
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
    }
    }//GEN-LAST:event_addpdActionPerformed

