    private void clearAllFieldsAndData() {
        // 1. Clear customer fields
        TO.setText("");
        num.setText("");
        add.setText("");
        pin.setText("");
        
        // 2. Clear product entry fields
        produc.setText("");
        price.setText("");
        qyt.setText("");
        
        // 3. Truncate the database table
        try {
            // TRUNCATE is faster than DELETE and resets the auto-increment ID (Sr. No.)
            String sql = "TRUNCATE TABLE products";
            PreparedStatement pst = con.prepareStatement(sql);
           pst.executeUpdate();
            
           loadProducts(); // Refresh the table (it will be empty)
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error clearing invoice data: " + e.getMessage());
        }
    }
private void loadProducts() {
    try {
        String sql = "SELECT id, PRODUCT_NAME, PRICE, QYNT, GST, TOTAL FROM products ORDER BY id ASC";
        PreparedStatement pst = con.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(
            new String[]{ "Sr No", "Product Name", "Price", "Quantity", "GST (%)", "Total (‚Çπ)" }, 0
        );

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("PRODUCT_NAME"),
                rs.getDouble("PRICE"),
                rs.getInt("QYNT"),
                rs.getDouble("GST") + "%",
                rs.getDouble("TOTAL")
                     
            });
        }

        product.setModel(model);

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Error loading products: " + e.getMessage());
    }

}

public PageFormat getPageFormat(PrinterJob pj){

        PageFormat pf =pj.defaultPage();
        Paper paper = pf.getPaper();
        double bodyHeight = bheight;
        double headerHeight = 5.0;
        double footerHeight = 5.0;
        double width = cm_to_pp(9);
        double height = cm_to_pp(headerHeight+bodyHeight+footerHeight);
        paper.setSize(width, height);
        paper.setImageableArea(0,10,width,height -cm_to_pp(1));
         
        pf.setOrientation(PageFormat.PORTRAIT);
        pf.setPaper(paper);
    
        return pf;   
}
protected static double cm_to_pp(double cm){
    return toPPI(cm*0.393600787);
}
protected static double toPPI(double inch){
    return inch*72d;
}

