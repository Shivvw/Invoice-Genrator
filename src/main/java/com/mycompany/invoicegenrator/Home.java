    private double calculateGrandTotal() {
        javax.swing.table.TableModel model = product.getModel();
        double grandTotal = 0.0;
        for (int i = 0; i < model.getRowCount(); i++) {
            // The 'Total' column is at index 5
            Object totalObj = model.getValueAt(i, 5);
            if (totalObj instanceof Double) {
                grandTotal += (Double) totalObj;
            }
            // Add other necessary checks for String, etc., if model types vary
        }
        return grandTotal;
    }

