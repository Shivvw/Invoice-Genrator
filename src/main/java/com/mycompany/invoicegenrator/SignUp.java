boolean validation()
{
    name=Name.getText();
    un=UN.getText();
    pw=PW.getText();
    cn=CN.getText();
    dob=DOB.getDate();
    
    if(name.equals(""))
    {
    JOptionPane.showMessageDialog(this, "Please Eneter Name..... ");
    return false;
    }
    if(un.equals(""))
    {
    JOptionPane.showMessageDialog(this, "Please Eneter User Name..... ");
    return false;
    }
    if(pw.equals(""))
    {
    JOptionPane.showMessageDialog(this, "Please Eneter Password..... ");
    return false;
    }
    if(cn.equals(""))
    {
    JOptionPane.showMessageDialog(this, "Please Eneter mobile No..... ");
    return false;
    }    
    if(dob.equals(null))
    {
    JOptionPane.showMessageDialog(this, "Please Select date ..... ");
    return false;
    }
  return true;
}
