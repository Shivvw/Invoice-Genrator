void InsertData(){
    SimpleDateFormat s1=new SimpleDateFormat("yyyy-MM-dd");
    String dob1=s1.format(dob);
    try{
    Class.forName("com.mysql.cj.jdbc.Driver");
    String url="jdbc:mysql://localhost:3306/sfmsdb?zeroDateTimeBehavior=CONVERT_TO_NULL";
    Connection con=DriverManager.getConnection(url,"root","Shubham@1234");
    
    String sql="insert into signup values(?,?,?,?,?,?)";
        PreparedStatement st=con.prepareStatement(sql);
       
       st.setInt(1, getId()); 
       st.setString(2,name);
       st.setString(3,un); 
       st.setString(4,pw); 
       st.setString(5,cn);
       st.setString(6,dob1);
       int i=st.executeUpdate();
       if(i>0){
       JOptionPane.showMessageDialog(this, "Record Insert Succesfully ..... ");
       }
       else{JOptionPane.showMessageDialog(this, "Not Inserted");}
    }
    catch (Exception e){
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error"+e.getMessage());
    }
}
